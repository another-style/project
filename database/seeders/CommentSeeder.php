     * Одноразовый сидер: ~900 фейковых комментариев
     * в стиле двачевских обсуждений про IT, LLM, вайбкодинг и безработных айтишников.
     * Вложенность до 6-7 уровней.
     *
     * Формат replies: [parentIndex, 'message']
     * parentIndex — индекс в массиве allNodes (0 = корневой комментарий треда,
     * 1 = первый ответ, 2 = второй ответ и т.д.)
     */
    public function run(): void
    {
        $topics = $this->getTopics();
        $baseDate = Carbon::parse('2026-01-10 08:00:00');

        foreach ($topics as $topicIdx => $topic) {
            $threadDate = $baseDate->copy()->addDays($topicIdx * 2)->addMinutes(rand(0, 720));

            $root = Comment::create([
                'name' => null,
                'message' => $topic['root'],
                'ip_address' => $this->randomIp(),
                'created_at' => $threadDate,
                'updated_at' => $threadDate,
            ]);

            $allNodes = [$root];
            $currentDate = $threadDate->copy();

            foreach ($topic['replies'] as [$parentIdx, $message]) {
                $currentDate->addMinutes(rand(2, 30));
                $parent = $allNodes[min($parentIdx, count($allNodes) - 1)];

                $child = new Comment([
                    'name' => null,
                    'message' => $message,
                    'ip_address' => $this->randomIp(),
                    'created_at' => $currentDate->copy(),
                    'updated_at' => $currentDate->copy(),
                ]);
                $child->appendToNode($parent)->save();
                $allNodes[] = $child;
            }

            // Обновить last_comment_at у корневого комментария
            $lastReplyDate = $currentDate->copy();
            $root->update(['last_comment_at' => count($topic['replies']) > 0 ? $lastReplyDate : $threadDate]);
        }
    }

    private function randomIp(): string
    {
        return mt_rand(1, 223) . '.' . mt_rand(0, 255) . '.' . mt_rand(0, 255) . '.' . mt_rand(1, 254);
    }

